    private String[] buildArgs() {
        String args = "";
        
        ArrayList<String> params = new ArrayList<String>();
        params.add(this.getParameter("param1"));
        params.add(this.getParameter("param2"));
        params.add(this.getParameter("param3"));
        params.add(this.getParameter("param4"));
        params.add(this.getParameter("param5"));
        params.add(this.getParameter("param6"));
        params.add(this.getParameter("param7"));
        params.add(this.getParameter("param8"));
        params.add(this.getParameter("param9"));
        params.add(this.getParameter("param10"));
        params.add(this.getParameter("param11"));
        params.add(this.getParameter("param12"));
        params.add(this.getParameter("param13"));
        
        
        for (String s : params) {
            if (s != null && !s.equals("")) {
                args = args + "\n" + s;
            }
        }
        
        return args.split("\n");
    }

