    @Action
    public Task printCard() {
        printButton.setEnabled(false);
        PrintCardTask task = null;
        try {
             task = new PrintCardTask(org.jdesktop.application.Application.getInstance(jzebra.JZebraApp.class));
        }
        catch (Exception e) {
            JZebra.showError("A thread exception has been thrown trying to print your card:  " + 
                        e.getMessage() + ".   More details can be found in the Java Console.  " +
                        "If problem persists, close and reopen web browser.");
        }
        printButton.setEnabled(true);
        return task;
    }

    /**
     * 
     */
    private class PrintCardTask extends org.jdesktop.application.Task<Object, Void> {
        boolean error = false;
        PrintCardTask(org.jdesktop.application.Application app) {
            super(app);
        }
        @Override protected Object doInBackground() {
            JZebra.setCommands(JZebra.readFile("/jzebra/resources/eltron_p310.cfg"));
            JZebra.processArgs(JZebraApplet.this.buildArgs());
            error = !JZebra.print();
            return null;  // return your result
        }
        @Override protected void succeeded(Object result) {
            if (error) {
                System.out.println("Critical error printing your card");
                JZebra.showError("Critical error printing your card.  " + 
                        " Check to ensure your printer is configured properly.");
            }
            else {
                JZebra.showSuccess("Successfully sent card information to printer.");
            }
            printButton.setEnabled(true);
        }
    }

